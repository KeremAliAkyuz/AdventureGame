
public class Vampire extends Monster{
	
	private int health=14;
	private int damage=4;
	private int gold=7;
	
	public int getVampireHealth() {
		return health;
	}
	public void setVampireHealth(int health) {
		this.health = health;
	}
	public int getVampireDamage() {
		return damage;
	}
	public void setVampireDamage(int damage) {
		this.damage = damage;
	}
	public int getVampireGold() {
		return gold;
	}
	public void setVampireGold(int gold) {
		this.gold = gold;
	}

}


