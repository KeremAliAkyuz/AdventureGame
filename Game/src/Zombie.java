
public class Zombie extends Monster{
	
	private int health=10;
	private int damage=3;
	private int gold=4;
	


	public int getZombieHealth() {
		return health;
	}


	public void setZombieHealth(int health) {
		this.health = health;
	}


	public int getZombieDamage() {
		return damage;
	}


	public void setZombieDamage(int damage) {
		this.damage = damage;
	}


	public int getZombieGold() {
		return gold;
	}


	public void setZombieGold(int gold) {
		this.gold = gold;
	}

}
