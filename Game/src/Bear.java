
public class Bear extends Monster{
	
	private int health=20;
	private int damage=7;
	private int gold=12;
	
	public int getBearHealth() {
		return health;
	}
	public void setBearHealth(int health) {
		this.health = health;
	}
	public int getBearDamage() {
		return damage;
	}
	public void setBearDamage(int damage) {
		this.damage = damage;
	}
	public int getBearGold() {
		return gold;
	}
	public void setBearGold(int gold) {
		this.gold = gold;
	}

}
